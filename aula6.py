# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, int, float, bool

print(1 + 1)
print(1 + 1.5)
print('a' + 'b')
print(1 + float('5'))
print(type(1 + float('5')))
print(bool('')) # False
print(bool('1')) # True

a = 5

print(str(a) + ' Gatos')
print(type(a))